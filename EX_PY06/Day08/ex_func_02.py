# 사용자 정의 함수
# 함수 기능 : 2개인 정수를 덧셈한 후 결과출력
# 함수 이름 : add
# 매개 변수 : 2개, num1, num2
# 함수 결과 : result = num1 + num2

# 함수 생성
def add(num1, num2):
    result = num1 + num2
    print(result)

# 함수 호출
add(1, 2)

