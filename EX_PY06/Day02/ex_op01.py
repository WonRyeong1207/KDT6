# 연산자
# 1. 산술 연산자
#   - 종류 : +, -, *, /, //, %, **

num1 = 8
num2 = 3

print(f"{num1} + {num2} = {num1+num2}")
print(f"{num1} - {num2} = {num1-num2}")
print(f"{num1} * {num2} = {num1*num2}")
print(f"{num1} / {num2} = {num1/num2}")
print(f"{num1} // {num2} = {num1//num2}")
print(f"{num1} % {num2} = {num1%num2}")
print(f"{num1} ** {num2} = {num1**num2}")
print("\n\n")

# 2. 비교연산자
#   - 종류 : >, <, <=, >=, ==, !=, is, is not

num1, num2 = 'a', 'f'

print(f"{num1} > {num2} = {num1>num2}")
print(f"{num1} < {num2} = {num1<num2}")
print(f"{num1} >= {num2} = {num1>=num2}")
print(f"{num1} <= {num2} = {num1<=num2}")
print(f"{num1} == {num2} = {num1==num2}")
print(F"{num1} != {num2} = {num1!=num2}")
print(f"{num1} is {num2} = {num1 is num2}")
print(f"{num1} is not {num2} = {num1 is not num2}")

